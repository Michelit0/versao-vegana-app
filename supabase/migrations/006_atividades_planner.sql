create or replace view public.vw_resumo_atividades as
select
  count(*) filter (where ativo and status <> 'finalizado') as atividades_abertas,
  count(*) filter (where ativo and status <> 'finalizado' and prazo < current_date) as atividades_atrasadas,
  count(*) filter (where ativo and status <> 'finalizado' and prazo = current_date) as vencem_hoje,
  count(*) filter (where ativo and status <> 'finalizado' and prazo between current_date and current_date + interval '2 day') as vencem_em_ate_2_dias,
  count(*) filter (where ativo and prioridade in ('alta', 'urgente') and status <> 'finalizado') as alta_prioridade,
  count(*) filter (where ativo and status = 'finalizado' and data_conclusao >= date_trunc('week', now())) as finalizadas_semana
from public.atividades;
